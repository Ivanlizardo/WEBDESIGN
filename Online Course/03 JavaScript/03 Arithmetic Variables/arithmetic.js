// operator precedence = the order in which operators are evaluated
// 1. Parentheses
// 2. Exponentiation (**)
// 3. Multiplication (*), Division (/), and Modulus (%)
// 4. Addition (+) and Subtraction (-)

let result = 2 + 3 * 4 - 5 / 2 ** 2;
console.log(result); 
